namespace Xim.Simulators
{
    /// <summary>
    /// Contains values that describe simulator state.
    /// </summary>
    public enum SimulatorState
    {
        /// <summary>
        /// Simulator is stopped.
        /// </summary>
        Stopped,

        /// <summary>
        /// Simulator is starting.
        /// </summary>
        Starting,

        /// <summary>
        /// Simulator is running.
        /// </summary>
        Running,

        /// <summary>
        /// Simulator is stopping.
        /// </summary>
        Stopping
    }
}
